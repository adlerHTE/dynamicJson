package com.adler.dynamicJson.exception;

public class InvalidPropertyName extends Exception {

	private static final long serialVersionUID = 81909679360477329L;

	public InvalidPropertyName() {
		super();
	}

	public InvalidPropertyName(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InvalidPropertyName(String arg0) {
		super(arg0);
	}

}
